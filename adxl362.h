#ifndef _ADXL362_H_
#define _ADXL362_H_

#include <stdbool.h>
#include <stdint.h>

//ADXL362 - ACCELEROMETER
//REGISTERS
#define ADXL362_DEVID_AD						0x00
#define ADXL362_DEVID_MST						0x01

#define ADXL362_SOFT_RESET						0x1F

#define ADXL362_THRESH_ACT_L					0x20
#define ADXL362_THRESH_ACT_H					0x21
#define ADXL362_TIME_ACT						0x22

#define ADXL362_THRESH_INACT_L					0x23
#define ADXL362_THRESH_INACT_H					0x24
#define ADXL362_TIME_INACT_L					0x25
#define ADXL362_TIME_INACT_H					0x26
#define ADXL362_ACT_INACT_CTL					0x27

#define ADXL362_INTMAP1							0x2A
#define ADXL362_INTMAP2							0x2B

#define ADXL362_POWER_CTL						0x2D

#define ADXL362_SET_WAKEUP_START				0x0A

//ADXL362_SOFT_RESET PARAMETERS
#define ADXL362_SOFT_RESET_VALUE				0x52

//ADXL362_ACT_INACT_CTL PARAMETERS
#define ADXL362_SET_MOTION_DETECT_MODE			0x3F
#define ADXL362_LINK_MODE						0x10
#define ADXL362_LOOP_MODE						0x30
#define ADXL362_INACT_REF						0x08
#define ADXL362_INACT_EN						0x04
#define ADXL362_ACT_REF							0x02
#define ADXL362_ACT_EN							0x01

//ADXL362_INTMAP2 PARAMETERS
#define ADXL362_INTMAP2_AWAKE					0x40
#define ADXL362_INTMAP2_ACT_INT					0x10

//ADXL362_POWER_CTL PARAMETERS
#define ADXL362_POWER_CTL_WAKEUP				0x08
#define ADXL362_POWER_CTL_AUTOSLEEP				0x04
#define ADXL362_POWER_CTL_MEASUREMENT_MODE		0x02



//VALUES
#define ADXL362_DEVID_AD_VALUE					0xAD
#define ADXL362_DEVID_MST_VALUE					0x1D



#define ADXL362_SERIAL_PORT_SPI					0x00
#define ADXL362_SERIAL_PORT_I2C					0x01

//COMMANDS
#define ADXL362_WRITE_REGISTER					0x0A
#define ADXL362_READ_REGISTER					0x0B
#define ADXL362_READ_FIFO						0x0D


bool initAdxl362(uint8_t , uint8_t );
bool sleepAdxl362(void);

#endif